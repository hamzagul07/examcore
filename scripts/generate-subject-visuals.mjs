#!/usr/bin/env node
/** Generate slug→family maps and step-synced diagram specs from lesson JSON. */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..')
const contentRoot = path.join(ROOT, 'content/courses')
const outPath = path.join(ROOT, 'lib/courses/generated/subject-visuals.ts')

const SUBJECTS = ['9706', '9609', '9084', '9699', '9990']

function readLessons(code) {
  const dir = path.join(contentRoot, code)
  return fs
    .readdirSync(dir)
    .filter((f) => f.endsWith('.json') && !f.includes('.pilot.'))
    .map((f) => JSON.parse(fs.readFileSync(path.join(dir, f), 'utf8')))
}

function truncate(s, max) {
  const t = String(s).replace(/\*\*/g, '').replace(/\s+/g, ' ').trim()
  return t.length <= max ? t : `${t.slice(0, max - 1)}…`
}

function family9706(slug) {
  if (/cvp|break-even/.test(slug)) return 'commerce-breakeven'
  if (/ratio/.test(slug)) return 'commerce-ratios'
  if (/cost|marginal|absorption|profit|materials|labour|costing/.test(slug)) {
    return 'commerce-accounting-cost'
  }
  if (
    /financial|adjustment|limited|partnership|sole-trader|capital-and-revenue|changing-asset|users-of-accounting|draft/.test(
      slug
    )
  ) {
    return 'commerce-accounting-statements'
  }
  return 'commerce-accounting-ledger'
}

function family9609(slug) {
  if (/break-even/.test(slug)) return 'commerce-breakeven'
  if (/ratio|liquidity|profitability|accounting-data/.test(slug)) return 'commerce-ratios'
  if (/investment-appraisal|payback|arr|npv/.test(slug)) return 'commerce-investment'
  if (/stakeholder/.test(slug)) return 'commerce-stakeholder'
  if (/elasticity/.test(slug)) return 'commerce-elasticity'
  const unit = parseInt(slug.split('-')[0], 10)
  if (unit === 3) return 'commerce-marketing'
  if (unit === 2 || unit === 7) return 'commerce-hrm'
  if (unit === 4 || unit === 9) return 'commerce-operations'
  if (unit === 5 || unit === 10) return 'commerce-finance'
  return 'commerce-strategy'
}

function family9084(slug) {
  if (/^2-/.test(slug)) return 'law-criminal-elements'
  if (/^4-/.test(slug)) return 'law-tort-elements'
  if (/^3-/.test(slug)) return 'law-contract-elements'
  return 'law-system-process'
}

function family9699(slug) {
  if (/^2-/.test(slug)) return 'soc-research'
  return 'soc-theory'
}

function family9990(slug) {
  if (/^4-/.test(slug)) return 'psych-workplace'
  if (/^3-/.test(slug)) return 'psych-health'
  if (/^2-/.test(slug)) return 'psych-consumer'
  return 'psych-clinical'
}

const classifiers = {
  '9706': family9706,
  '9609': family9609,
  '9084': family9084,
  '9699': family9699,
  '9990': family9990,
}

function buildSteps(lesson) {
  const fromSimple = lesson.simpleExplanation?.steps ?? []
  const fromObjectives = lesson.learningObjectives ?? []
  const fromKeyPoints =
    lesson.sections?.find((s) => s.type === 'keyPoints')?.type === 'keyPoints'
      ? lesson.sections.find((s) => s.type === 'keyPoints').items
      : []

  const pool =
    fromSimple.length >= 3
      ? fromSimple
      : fromKeyPoints.length >= 3
        ? fromKeyPoints
        : fromObjectives.length >= 3
          ? fromObjectives
          : [
              `Define the core idea: ${lesson.title}.`,
              `Explain how ${lesson.topicCode} works in exam answers.`,
              `Apply the concept to a typical Cambridge scenario.`,
              `Evaluate limitations and common mistakes for ${lesson.paper}.`,
            ]

  return pool.slice(0, 4).map((text, i) => ({
    caption: truncate(text, 140),
    embedHint: truncate(`Step ${i + 1}: ${text}`, 100),
  }))
}

function specEntry(steps) {
  const lines = steps.map(
    (s, i) =>
      `      { focus: ['step-${i + 1}'], caption: ${JSON.stringify(s.caption)}, embedHint: ${JSON.stringify(s.embedHint)} }`
  )
  return `    steps: [\n${lines.join(',\n')},\n    ]`
}

const slugFamilies = {}
const specs = {}

for (const code of SUBJECTS) {
  const classify = classifiers[code]
  for (const lesson of readLessons(code)) {
    const family = classify(lesson.slug)
    slugFamilies[lesson.slug] = family
    specs[lesson.slug] = buildSteps(lesson)
  }
}

const familyBlocks = SUBJECTS.map((code) => {
  const entries = Object.entries(slugFamilies).filter(([slug]) =>
    readLessons(code).some((l) => l.slug === slug)
  )
  const lines = entries.map(([slug, family]) => `  '${slug}': '${family}',`)
  return `export const SLUG_FAMILY_${code}: Record<string, keyof typeof COMMERCE_HUMANITIES_FAMILIES> = {\n${lines.join('\n')}\n}`
})

const specLines = Object.entries(specs).map(
  ([slug, steps]) => `  '${slug}': {\n${specEntry(steps)},\n  }`
)

const output = `/** Auto-generated by scripts/generate-subject-visuals.mjs — do not edit by hand. */
import type { LessonDiagramSpec } from '@/lib/courses/diagram-specs'

export const GENERATED_SUBJECT_SPECS: Record<string, LessonDiagramSpec> = {
${specLines.join(',\n')}
}

${familyBlocks.join('\n\n')}

export const SLUG_FAMILY_COMMERCE_HUMANITIES: Record<string, keyof typeof COMMERCE_HUMANITIES_FAMILIES> = {
  ...SLUG_FAMILY_9706,
  ...SLUG_FAMILY_9609,
  ...SLUG_FAMILY_9084,
  ...SLUG_FAMILY_9699,
  ...SLUG_FAMILY_9990,
}

/** Family ids referenced by generated slug maps (diagram-families.ts). */
export type CommerceHumanitiesFamilyId =
  | 'commerce-accounting-ledger'
  | 'commerce-accounting-cost'
  | 'commerce-accounting-statements'
  | 'commerce-breakeven'
  | 'commerce-ratios'
  | 'commerce-investment'
  | 'commerce-stakeholder'
  | 'commerce-elasticity'
  | 'commerce-marketing'
  | 'commerce-hrm'
  | 'commerce-operations'
  | 'commerce-finance'
  | 'commerce-strategy'
  | 'law-system-process'
  | 'law-contract-elements'
  | 'law-criminal-elements'
  | 'law-tort-elements'
  | 'soc-theory'
  | 'soc-research'
  | 'psych-clinical'
  | 'psych-consumer'
  | 'psych-health'
  | 'psych-workplace'

export const COMMERCE_HUMANITIES_FAMILIES = {} as Record<CommerceHumanitiesFamilyId, true>
`

fs.mkdirSync(path.dirname(outPath), { recursive: true })
fs.writeFileSync(outPath, output)
console.log(`Wrote ${Object.keys(specs).length} specs to ${path.relative(ROOT, outPath)}`)
