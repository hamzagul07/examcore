#!/usr/bin/env node
/**
 * Regenerates lib/courses/syllabus-objectives/index.ts from whatever <code>.json
 * outcome files are present. Run after adding/removing a subject's outcomes so
 * the lesson "Syllabus coverage" section picks it up with no manual wiring.
 */
import { readdirSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const dir = join(ROOT, 'lib', 'courses', 'syllabus-objectives')

const codes = readdirSync(dir)
  .filter((f) => /^\d+\.json$/.test(f))
  .map((f) => f.replace('.json', ''))
  .sort()

const imports = codes.map((c) => `import o${c} from './${c}.json'`).join('\n')
const entries = codes.map((c) => `  '${c}': o${c} as Outcome[],`).join('\n')

const out = `// AUTO-GENERATED by scripts/gen-syllabus-index.mjs — do not edit by hand.
// Run \`node scripts/gen-syllabus-index.mjs\` after changing the outcome files.
${imports}

export type Outcome = { code: string; topic: string; text: string }

export const SYLLABUS_OUTCOMES: Record<string, Outcome[]> = {
${entries}
}
`

writeFileSync(join(dir, 'index.ts'), out)
console.log(`Wrote index.ts with ${codes.length} subjects: ${codes.join(', ')}`)
